struct Recipe: Identifiable {
    /// 레시피 고유 번호
    var id: Int

    /// 레시피 이름 (예: 계란말이)
    var title: String

    /// 레시피 간단 설명 (예: 계란말이, 또는 달걀말이는 달걀을 팬에 얇고 넓게 부친 뒤 만 음식이다. 반찬이나 술안주로 먹는다. 계란만 이용하기도 하지만 주로 야채 등을 다져 넣거나 김을 넣기도 한다.)
